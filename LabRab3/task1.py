# Отримати послідовність символів від 18-го символа і до 3-го з кінця (3-й символ з кінця не включається).

line = str(input("Введіть довільний рядок, який має не менше 18 символів: "))

while (len(line) < 18):

    line = str(input("Введіть ще раз рядок, так як він має менше 18 символів: "))

print("Після зрізу першого символу та останього: ", line[18:-3] )