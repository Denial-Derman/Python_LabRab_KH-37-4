# Задано речення. Скласти програму, яка визначає і виводить на екран слова які містять три літери «е».

sentence=str(input("Введіть речення довільної довжини: "))

words=sentence.split()
# Занесення кожного слово, що містить 3 'е' в окремий список
resultWords = [word for word in words if word.count('е') == 3]

# Використано метод .join() для об'єднання слів у рядок
print(f"Слова з речення які містять три літери «е»: ", ", ".join(resultWords))